    <h4>PHP 開発促進・国際化</h4>
    <p>PHP 国際化プロジェクトが結成されています。
      PHP 言語自体の開発について情報交換を行うメーリングリスト (php-dev) が提供されており、
      mbstring など既に php.net 上で開発されている国際化 (i18n) 部分も含めた
      日本語 (l10n)環境の向上のための議論をはじめとして、 
      PHP 言語自体の開発について情報交換が行われています。</p>
    <p>これまでの、PHP 国際化に関する成果については、
      <a href="http://old.php.gr.jp/project/i18n/" rel="nofollow">こちら</a>をご覧ください。</p>

    <h4>ドキュメント翻訳プロジェクト</h4>
    <p>主に php.net で提供されているドキュメントの翻訳を行うプロジェクトです。
      現在、<a href="http://jp.php.net/manual/en/" rel="nofollow">PHP Manual</a> および
      <a href="http://pear.php.net/manual/en/" rel="nofollow">PEAR Manual</a>
      の翻訳が行われています。</p>
    <p>翻訳をはじめとして、日本語ドキュメントに関して情報交換を行うメーリングリスト (php-doc) 
      が提供されています。詳細については、
      <a href="http://docs.php.gr.jp/">docs.php.gr.jp</a>をご覧ください。
      翻訳活動への参加もお待ちしております。</p>

    <h4 id="content_1_4">PHP にまつわる情報の収集と配信</h4>
    <p>PHP 自体のリリース情報や付随するライブラリなどのアップデート情報、その他書籍や事例などを配信します。</p>
    <p>詳細については、<a href="http://news.php.gr.jp/">news.php.gr.jp</a> をご覧ください。</p>

    <h5>PHP Planet Japaneseについて</h5>
    <p>一般的に PHP に関連する情報をブログなどで配信しているサイトを運営している場合、RSS や ATOM などで情報配信を受けることができます。日本 PHP ユーザ会では PHP に関連する情報を収集する方法のひとつとして「PHP
      Planet Japanese」を運営します。これは「PHP 関連に限った情報配信ができる」ことを前提に自由に自分のサイトを登録できる Planet になります。</p>
    <p>雑多な情報を扱うブログなどの場合、PHP に限ったカテゴリの配信などの機能がない場合、配信登録を拒否させていただく場合があります。</p>

    <h4>掲示板・メーリングリストなどを用いた双方向の情報交換</h4>
