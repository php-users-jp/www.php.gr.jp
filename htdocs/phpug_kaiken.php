<h1 align=center>日本PHPユーザ会</h1>
<p align=center>http://www.php.gr.jp/</p>

<p>日本PHPユーザ会（略称  PHP-JP）を2000年4月19日に発足いたします。</p>
<p>日本PHPユーザ会（以下、ユーザ会）は、PHPの普及と発展を目指し、以下の活動
を行います。</p>
<ul>
  <li>PHPに関する研究開発および普及促進
  <li>PHPに関する情報の公開
  <li>会員相互および外部との技術的・人間的交流
</ul>

<p>  ユーザ会の発足に伴い、PHPに関する各ワーキンググループの立ち上げを行います。まずは、ユーザ会の窓口であるホームページの整備を行う「PHP-WEBワーキンググループ、及び、PHPに関する情報交換・議論のためのメーリングリストを運営する「PHP-MLワーキンググループ」を立ち上げます。これらは、php.gr.jpのドメインで運営されます。</p>
<p>  また、PHPに関するライター及び技術者を中心に、PHPのドキュメントを整備する「PHP-DOCワーキンググループ」、国際化などの技術的テーマを扱う「PHP-DEVワーキンググループ」を作り、最新動向やソースの取得、技術的な議論が可能な場を立ち上げます。</p>
<br />
<p>  将来的には、各種データベースベンダー様や他のユーザ会とも連携、情報収集し、事例集など各種情報の発信源になるとともに、PHPの開発が可能な業者や開発者自体のデータベースを持ち、PHPによるアプリケーション開発の活性化を目指します。</p>
