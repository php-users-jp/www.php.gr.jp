<h1 align=center>ワーキンググループの御案内</h1>
</br>
<p>  ユーザ会の発足に伴い、PHPに関する各ワーキンググループの立ち上げを行います。<br />
  すぺてのワーキンググループは、http://www.php.gr.jp/ のホームページからアクセス可能です。<br />
  現在、発足予定のワーキンググループは以下の通りです。</p>
<br />

<dl>
<dt>・[PHP-WEB]
<dd>本WGの活動は、PHPをつかった事例紹介 アプリケーション例、イベント紹介等をウェブ上で提供していくことを目的とします。その他 FAQの検索、スクリプト事例集などの充実をはかっていきます。（豊倉）<br />
<br />

<dt>・[PHP-ML]
<dd>本WGの活動は，PHPに関する情報交換や議論のためのメーリングリストを運営することです．英語は苦手で本家のメーリングリストは敷居が高いと思う方や，PHPの日本語に関する問題などPHPに関心のある方ならどなたでも自由に参加できます．また，メーリングリスト内での有用な情報やFAQなどをドキュメント整備WGと共に整備することも行います．（山本哲男）<br />
<br />

<dt>・[PHP-DOC]
<dd>本WGの活動は、PHPアプリケーション構築を行う際に有用な日本語の文書情報を作成・整備し、広く配布・提供することを目的とします。具体的にはPHPマニュアル等の翻訳・保守、FAQや初心者向けインストール手順書の整備等を行います。（広川類）<br />
<br />

<dt>・[PHP-DEV]
<dd>PHPは主に英語環境で使用することを想定して開発されています。これに日本語やその他の各国語の環境で使用するための機能を付加する開発を行います。すでに、日本語については、有志による「国際化チーム」によってある程度の成果を得ていますが、本WGではこの活動を引き継ぎます。（金本茂）<br />
<br />

<dt>・[PHP-EVENT]
<dd>本WGの活動は、各種イベントやセミナーにてPHPに関する情報を広く一般に提供し、PHPの普及促進を図っていくことを目的としています。外部企業、団体主催のイベント・セミナーはもちろん、PHPの普及・活用を促進するためのセミナーを自主的に開催していく予定です。（宮原徹）<br />
<br />

<dt>・[PHP-SPONSORS]
<dd>日本PHPユーザ会は、会費を徴収しない代わりに、イベント毎に必要な機器・会場・資金を募ります。PHPの活動を支援されたい方々のために、PHP-SPONSORSというメーリングリストを作成し、イベント毎に、何が必要かをそのメーリングリストに投げます。ユーザ会を支援したい方がいらっしゃいましたら、随時PHP-SPONSORSメーリングリストに参加していただき、支援可能な場合に、それに応じていただくような形式で運営していきます。（宮本和明）<br />
</dl>
