				<h2>PHP 5について</h2>
				<p>現在、PHP 4、PHP 5の2系統での開発が行われています。PHP 5はPHP 4と比較して、オブジェクト指向機能が大幅に改良され、他にも数多くの新機能や改良が盛り込まれています。ただし下位互換性は保たれないため、PHP 4のために書かれたコードをPHP
		  5に移行するには注意が必要です。</p>
