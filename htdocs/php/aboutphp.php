				<h2>PHPについて</h2>
				<p>PHPは、HTMLファイル内に記述するタイプのスクリプト言語です。通常のCGIとして使用できますが、PHPモジュールをApacheサーバーに組み込むことにより、 Perl/CGIと比較して処理速度の高速化、サーバー負荷の低減が可能です。</p>
				<p>PHPはPostgreSQL、MySQL、Oracle、Sybaseといった各種データベースとの連携に優れています。他にも、XML、PDF、IMAP、LDAP等各種機能をサポートしており、広範なWebアプリケーションを容易に作成可能です。</p>
			</div>
			<div class="section">
				<h2>動作環境・インストールについて</h2>
				<p>PHPは、多くのUNIX系システム（Linuxを含む）、MacOS X、Windowsシステムで動作可能です。各システムでのインストールに関しては、<a href="http://www.php.net/manual/ja/install.php">http://www.php.net/manual/ja/install.php</a>を参考にしてください。</p>
				<p>また多くのLinuxディストリビューションでは、プラットフォーム別にパッケージ化されたPHPが用意されています。詳しくは、各ディストリビューションのパッケージを参照してください。</p>
			</div>
			<div class="section">
				<h2>ダウンロードについて</h2>
				<p>PHPの最新版は<a href="http://www.php.net/downloads.php">http://www.php.net/downloads.php</a>より入手できます。</p>
				<p>完全なソースコードの配布に加え、Windows版に関してはバイナリー形式での配布が行われています。</p>
				<p>また、随時更新される最新のスナップショット版は、<a href="http://snaps.php.net/">http://snaps.php.net/</a>より入手できます。</p>
