				<h2>日本PHPユーザー会について</h2>
				<p>PHPはWebアプリケーションを容易に作成できるスクリプト言語として、幅広く普及しています。さらに現在、データベースとの連携が注目され、その重要性を増 しつつあります。この状況を鑑み、PHPの研究開発や情報交換に取り組んでいるメンバーを中心に、以下の目的をより明確かつ効率的に実行するために日本PHPユーザー会を運営しています。</p>
				<div class="sub-section">
					<h3>ユーザー会の目的</h3>
					<p>日本PHPユーザー会は、以下の目的をもとに活動をしています。</p>
					<ul>
						<li>PHPの研究開発や、国際化、普及の促進を行う。</li>
						<li>PHPに関する情報の収集と公開、技術の移転の促進を、種々の手段を通じて行う。 </li>
					</ul>
				</div>
				<div class="sub-section">
					<h3>ユーザー会の活動内容</h3>
					<p>主な活動としては、以下のものが挙げられます。</p>
					<ul>
						<li>インターネットを利用した情報発信（当サイト）</li>
						<li>PHPの研究開発、情報収集に伴うメーリングリストの運営</li>
						<li>PHPに関する研究会や講習会の開催（PHPカンファレンス）</li>
						<li>その他、本会の目的を達成するために必要な事業</li>
					</ul>
				</div>
			</div>
